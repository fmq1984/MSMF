def jitter_point_cloud(batch_data, sigma=0.01, clip=0.05):#在原始点云数据集上通过标准正太分布（np.random.randn()）添加噪声，作为数据集增强的一种。
    """ Randomly jitter points. jittering is per point.
        Input:
          BxNx3 array, original batch of point clouds
        Return:
          BxNx3 array, jittered batch of point clouds
        噪声数据用np.clip()函数限幅。默认是在点的每一个维度（C：Channel）上添加噪声，也可以按需要修改代码
    """
    B, N, C = batch_data.shape
    assert(clip > 0)
    jittered_data = np.clip(sigma * np.random.randn(B, N, C), -1*clip, clip)
    jittered_data += batch_data
    return jittered_data

#这些函数用来加载h5py格式的数据集
def getDataFiles(list_filename):
    return [line.rstrip() for line in open(list_filename)]

def load_h5(h5_filename):
    f = h5py.File(h5_filename)
    data = f['data'][:]
    label = f['label'][:]
    return (data, label)

def loadDataFile(filename):
    return load_h5(filename)

def load_h5_data_label_seg(h5_filename):
    f = h5py.File(h5_filename)
    data = f['data'][:]
    label = f['label'][:]
    seg = f['pid'][:]
    return (data, label, seg)


def loadDataFile_with_seg(filename):
    return load_h5_data_label_seg(filename)


def rotate_perturbation_point_cloud(batch_data, angle_sigma=0.06,angle_clip=0.18):

    rotated_data = np.zero(batch_data.shape,dtype=np.float32)

    for k in range(batch_data.shape[0]):
        angles = np.clip(angle_sigma * np.random.randn(3),-angle_clip,angle_clip)
        Rx = np.array([
            [1,0,0],
            [0,np.cos(angles[0]),-np.sin(angles[0])],
            [0,np.sin(angles[0]),np.cos(angles[0])],
        ]
        )
        Ry = np.array(
            [
                [np.cos(angles[1]), 0, np.sin(angles[1])],
                [0, 1, 0],
                [-np.sin(angles[1]), 0, np.cos(angles[1])],
            ]
        )
        Rz = np.array(
            [
                [np.cos(angles[2]), -np.sin(angles[2]), 0],
                [np.sin(angles[2]), np.cos(angles[2]), 0],
                [0, 0, 1],
            ]
        )
        R = np.dot(Rz, np.dot(Ry, Rx))
        shape_pc = batch_data[k, ...]
        rotated_data[k, ...] = np.dot(shape_pc.reshape((-1, 3)), R)
    return rotated_data










